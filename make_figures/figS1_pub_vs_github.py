#!/usr/bin/env python3
"""
DEPRECATED — kept for archival reference only.

This figure (joint distribution of years since first publication and
years on GitHub, faceted by field) appeared in earlier drafts of the
Online Appendix's "Joint distribution" section. That section was
removed when the within-field-x-lag-bucket table (now Appendix A.12,
generated by scripts/within_field_adoption_by_bucket.py) replaced it
as the canonical field-heterogeneity exhibit. The script still runs,
but the resulting figure is not cited in the current manuscript.

Original purpose: disentangle the three claims in the main text:
  - §3.2  adoption rises with seniority (years since first pub)
  - §3.5  adopters have more years on GitHub
  - A.8   "scientist-first" types adopt more
"""
import json
import os
from collections import Counter
from datetime import datetime

import matplotlib
matplotlib.use("Agg")
import matplotlib.patches as mpatches
import matplotlib.pyplot as plt
import numpy as np

from figure_template import (
    DATA_DIR, OUT_DIR, DPI, PALETTE,
    C_HIGHLIGHT, C_ALL_USERS,
    add_source, add_subtitle,
)

ACTIVE_FILE = os.path.join(DATA_DIR, "active_scientists.json")
ACCOUNTS_FILE = os.path.join(DATA_DIR, "scientist_github_accounts.json")
REFERENCE_DATE = datetime(2026, 2, 28)


def main():
    with open(ACTIVE_FILE) as f:
        active = json.load(f)
    with open(ACCOUNTS_FILE) as f:
        accounts = json.load(f)

    rows = []
    for u, info in active["scientists"].items():
        ey = info.get("earliest_pub_year")
        a = accounts.get(u)
        if not ey or not a or not a.get("found"):
            continue
        ts = a.get("account_created")
        if not ts:
            continue
        try:
            gh_year = datetime.fromisoformat(ts.replace("Z", "+00:00")).year
        except Exception:
            continue
        pub_years = REFERENCE_DATE.year - int(ey)
        gh_years = REFERENCE_DATE.year - gh_year
        if pub_years < 0 or gh_years < 0:
            continue
        rows.append({
            "field": info.get("field") or "Unknown",
            "pub_years": pub_years,
            "gh_years": gh_years,
            "claude_user": bool(info.get("claude_user")),
        })
    print(f"Total scientists with both years: {len(rows):,}")

    # Top 6 fields by adopter count
    field_adopt = Counter(r["field"] for r in rows if r["claude_user"])
    fields = [f for f, _ in field_adopt.most_common(6)]
    print(f"Fields plotted: {fields}")

    # ----- Plot --------------------------------------------------------
    fig, axes = plt.subplots(2, 3, figsize=(14, 9),
                              sharex=True, sharey=True,
                              gridspec_kw={"hspace": 0.32, "wspace": 0.10})
    axes = axes.flatten()

    # Use 99th percentile to avoid wasted whitespace from a few outliers
    pooled = [r for r in rows if r["field"] in fields]
    x_p99 = np.percentile([r["pub_years"] for r in pooled], 99)
    y_p99 = np.percentile([r["gh_years"] for r in pooled], 99)
    axis_max = max(x_p99, y_p99) + 1

    for idx, field in enumerate(fields):
        ax = axes[idx]
        non = [r for r in rows if r["field"] == field and not r["claude_user"]]
        adopt = [r for r in rows if r["field"] == field and r["claude_user"]]

        ax.scatter([r["pub_years"] for r in non],
                   [r["gh_years"] for r in non],
                   s=10, color=C_ALL_USERS, alpha=0.20, edgecolors="none",
                   label=f"non-adopter (n={len(non):,})")
        ax.scatter([r["pub_years"] for r in adopt],
                   [r["gh_years"] for r in adopt],
                   s=24, color=C_HIGHLIGHT, alpha=0.90,
                   edgecolors="white", linewidths=0.5,
                   label=f"adopter (n={len(adopt):,})")

        # 45° line: where first pub year == first GitHub year
        ax.plot([0, axis_max], [0, axis_max],
                linestyle="--", color="#888888", linewidth=1, alpha=0.7)
        ax.text(axis_max - 0.5, axis_max - 0.5,
                "first pub = first GitHub",
                fontsize=7.5, color="#888888", ha="right", va="bottom",
                rotation=45, rotation_mode="anchor")

        # Light shaded annotations: "coder-first" below, "scientist-first" above
        ax.text(axis_max * 0.85, axis_max * 0.05,
                "scientist-first\n(GitHub after first pub)",
                fontsize=7.5, color="#888888", ha="right", va="bottom",
                style="italic")
        ax.text(axis_max * 0.05, axis_max * 0.85,
                "coder-first\n(GitHub before first pub)",
                fontsize=7.5, color="#888888", ha="left", va="top",
                style="italic")

        ax.set_xlim(0, axis_max)
        ax.set_ylim(0, axis_max)
        ax.set_aspect("equal", adjustable="box")
        ax.set_title(f"{field}  (n={len(non)+len(adopt):,}, adopters={len(adopt)})",
                     loc="left", fontsize=10.5, fontweight="bold")

        if idx % 3 == 0:
            ax.set_ylabel("Years on GitHub")
        if idx >= 3:
            ax.set_xlabel("Years since first publication")

    # Hide any unused panels
    for j in range(len(fields), len(axes)):
        axes[j].axis("off")

    # Shared legend
    legend_handles = [
        mpatches.Patch(color=C_ALL_USERS, alpha=0.55, label="Non-adopter"),
        mpatches.Patch(color=C_HIGHLIGHT, label="Claude Code adopter"),
    ]
    fig.legend(handles=legend_handles, loc="upper center", ncol=2,
               bbox_to_anchor=(0.5, 1.01), frameon=False, fontsize=11)

    fig.suptitle(
        "Joint distribution of publication tenure and GitHub tenure, by adopter status",
        fontsize=13, fontweight="bold", x=0.06, ha="left", y=1.04,
    )

    add_source(
        fig,
        f"Source: ORCID public data + GitHub Core API. "
        f"n = {sum(1 for r in rows if r['field'] in fields):,} active scientists across 6 fields. "
        f"Reference date: 28 February 2026.",
    )

    out_base = os.path.join(OUT_DIR, "figS1_pub_vs_github")
    fig.savefig(out_base + ".svg", format="svg", bbox_inches="tight")
    fig.savefig(out_base + ".png", dpi=DPI, bbox_inches="tight")
    plt.close(fig)
    print(f"Saved: {out_base}.svg + .png")

    # Print numerical summary
    print("\nWithin each field, adopter location relative to 45° line:")
    print(f"{'Field':30s}  {'n_adopt':>8}  {'%scientist-first':>18}  {'%coder-first':>14}")
    for field in fields:
        adopt = [r for r in rows if r["field"] == field and r["claude_user"]]
        if not adopt:
            continue
        sci_first = sum(1 for r in adopt if r["pub_years"] > r["gh_years"])
        coder_first = sum(1 for r in adopt if r["pub_years"] < r["gh_years"])
        print(f"  {field:28s}  {len(adopt):>8d}  "
              f"{sci_first/len(adopt)*100:>16.1f}%  "
              f"{coder_first/len(adopt)*100:>12.1f}%")


if __name__ == "__main__":
    main()
